#!/usr/bin/python3
word = "Holberton"
word_first_3 = word[:3]  # Slices the first 3 characters
word_last_2 = word[-2:]  # Slices the last 2 characters
middle_word = word[1:-1]  # Slices the string except for the first and last characters
print(f"First 3 letters: {word_first_3}")
print(f"Last 2 letters: {word_last_2}")
print(f"Middle word: {middle_word}")

